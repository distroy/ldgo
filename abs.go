package ldgo

import (
	"github.com/distroy/ldgo/ldmath"
)

func AbsInt(n int) int       { return ldmath.AbsInt(n) }
func AbsInt8(n int8) int8    { return ldmath.AbsInt8(n) }
func AbsInt16(n int16) int16 { return ldmath.AbsInt16(n) }
func AbsInt32(n int32) int32 { return ldmath.AbsInt32(n) }
func AbsInt64(n int64) int64 { return ldmath.AbsInt64(n) }

func AbsFloat32(n float32) float32 { return ldmath.AbsFloat32(n) }
func AbsFloat64(n float64) float64 { return ldmath.AbsFloat64(n) }
