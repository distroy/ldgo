package ldgo

import (
	"github.com/distroy/ldgo/ldcore"
)

func AbsInt(n int) int       { return ldcore.AbsInt(n) }
func AbsInt8(n int8) int8    { return ldcore.AbsInt8(n) }
func AbsInt16(n int16) int16 { return ldcore.AbsInt16(n) }
func AbsInt32(n int32) int32 { return ldcore.AbsInt32(n) }
func AbsInt64(n int64) int64 { return ldcore.AbsInt64(n) }

func AbsFloat32(n float32) float32 { return ldcore.AbsFloat32(n) }
func AbsFloat64(n float64) float64 { return ldcore.AbsFloat64(n) }
